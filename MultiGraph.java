import java.util.List;

public class MultiGraph {
	
	private List<Vertex> v;
	private List<Edge> e;
	
	public List<Vertex> getV() {
		return v;
	}

	public void setV(List<Vertex> v) {
		this.v = v;
	}

	public List<Edge> getE() {
		return e;
	}

	public void setE(List<Edge> e) {
		this.e = e;
	}
	
}
